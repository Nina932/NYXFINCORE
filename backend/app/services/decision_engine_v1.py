@dataclass
class BusinessAction:
    """A concrete business action with simulated impact and risk assessment."""
    action_id: str
    description: str
    category: str           # cost_reduction|revenue_growth|risk_mitigation|capital_optimization|operational_efficiency
    expected_impact: float  # estimated P&L impact (positive = improvement)
    implementation_cost: float
    roi_estimate: float     # expected_impact / implementation_cost
    risk_level: str         # low|medium|high|critical
    time_horizon: str       # immediate|short_term|medium_term|long_term
    prerequisites: List[str] = field(default_factory=list)
    source_signal: str = ""
    composite_score: float = 0.0
    simulation_result: Optional[Dict[str, Any]] = None

    def to_dict(self) -> Dict[str, Any]:
        return {
            "action_id": self.action_id,
            "description": self.description,
            "category": self.category,
            "expected_impact": round(self.expected_impact, 2),
            "implementation_cost": round(self.implementation_cost, 2),
            "roi_estimate": round(self.roi_estimate, 2),
            "risk_level": self.risk_level,
            "time_horizon": self.time_horizon,
            "prerequisites": self.prerequisites,
            "source_signal": self.source_signal,
            "composite_score": round(self.composite_score, 4),
            "simulation_result": self.simulation_result,
        }


@dataclass
class RiskMatrix:
    """Actions grouped by risk level for visual risk assessment."""
    low_risk_actions: List[Dict[str, Any]] = field(default_factory=list)
    medium_risk_actions: List[Dict[str, Any]] = field(default_factory=list)
    high_risk_actions: List[Dict[str, Any]] = field(default_factory=list)
    critical_risk_actions: List[Dict[str, Any]] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "low_risk": self.low_risk_actions,
            "medium_risk": self.medium_risk_actions,
            "high_risk": self.high_risk_actions,
            "critical_risk": self.critical_risk_actions,
        }


@dataclass
class DecisionReport:
    """Complete decision intelligence output with ranked actions, risk matrix, and CFO verdict."""
    top_actions: List[BusinessAction] = field(default_factory=list)
    total_actions_evaluated: int = 0
    risk_matrix: RiskMatrix = field(default_factory=RiskMatrix)
    total_potential_impact: float = 0.0
    health_score_before: float = 0.0
    projected_health_score: float = 0.0
    cfo_verdict: Optional[Any] = None  # CFOVerdict (typed as Any to avoid forward ref)
    generated_at: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "top_actions": [a.to_dict() for a in self.top_actions],
            "total_actions_evaluated": self.total_actions_evaluated,
            "risk_matrix": self.risk_matrix.to_dict(),
            "total_potential_impact": round(self.total_potential_impact, 2),
            "health_score_before": round(self.health_score_before, 1),
            "projected_health_score": round(self.projected_health_score, 1),
            "cfo_verdict": self.cfo_verdict.to_dict() if self.cfo_verdict else None,
            "generated_at": self.generated_at,
        }


