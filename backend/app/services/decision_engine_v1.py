"""
Phase I: Decision Intelligence Engine
======================================
Transforms diagnostic signals into ranked, simulated business actions.

Pipeline:
    DiagnosticReport -> ActionGenerator -> ImpactSimulator -> ActionRanker -> DecisionReport

Rules:
    - ALL financial math is deterministic (no LLM)
    - Actions derived from rule-based templates, never generated by AI
    - Impact simulated via existing financial_reasoning.simulate_scenario()
    - Composite scoring is a fixed, auditable formula

Reuses:
    - diagnosis_engine.DiagnosticReport  (input)
    - financial_reasoning.reasoning_engine.simulate_scenario()  (impact simulation)
"""

