class ActionGenerator:
    """Generates BusinessAction instances from DiagnosticReport signals using rule templates."""

    # Maps action category to scenario simulation parameters
    _CATEGORY_SCENARIO_MAP = {
        "cost_reduction": {"ga_pct": -15},
        "revenue_growth": {"revenue_pct": 10},
        "capital_optimization": {"finance_expense_pct": -20},
        "operational_efficiency": {"cogs_pct": -5},
        "risk_mitigation": {"cogs_pct": -2},
    }

    def generate_actions(self, report, financials: Dict[str, float]) -> List[BusinessAction]:
        """
        Generate actions from diagnosis signals.

        Enhanced: computes ROI by running actual financial simulation for each action,
        not just template percentages.
        """
        actions: List[BusinessAction] = []
        revenue = financials.get("revenue", 0) or 0

        # Handle report as dict or object
        if isinstance(report, dict):
            diagnoses = report.get("diagnoses", report.get("signals", []))
        else:
            diagnoses = getattr(report, "diagnoses", [])

        for diagnosis in diagnoses:
            if isinstance(diagnosis, dict):
                signal = diagnosis.get("signal", diagnosis)
                if isinstance(signal, dict):
                    metric = signal.get("metric", "")
                    direction = signal.get("direction", "down")
                else:
                    metric = getattr(signal, "metric", "")
                    direction = getattr(signal, "direction", "down")
            else:
                signal = diagnosis.signal
                metric = signal.metric
                direction = signal.direction

            templates = _ACTION_TEMPLATES.get((metric, direction), [])

            if not templates:
                tmpl = _FALLBACK_TEMPLATE.copy()
                tmpl["description"] = tmpl["description"].format(metric=metric)
                templates = [tmpl]

            for tmpl in templates:
                # Base impact estimate from template
                template_impact = abs(revenue) * tmpl["impact_pct"] if revenue else 0
                cost = tmpl["base_cost"]

                # Try to compute actual ROI via simulation
                sim_result = self._compute_action_roi(
                    tmpl["category"], cost, financials
                )

                if sim_result and sim_result.get("data_driven"):
                    impact = sim_result["expected_impact"]
                    roi = sim_result["roi_estimate"]
                    sim_data = sim_result
                else:
                    impact = template_impact
                    roi = round(impact / max(cost, 1), 2)
                    sim_data = None

                action = BusinessAction(
                    action_id=f"{tmpl['category']}_{metric}_{uuid.uuid4().hex[:6]}",
                    description=tmpl["description"],
                    category=tmpl["category"],
                    expected_impact=round(impact, 2),
                    implementation_cost=cost,
                    roi_estimate=roi,
                    risk_level=tmpl["risk"],
                    time_horizon=tmpl["horizon"],
                    prerequisites=list(tmpl.get("prerequisites", [])),
                    source_signal=f"{metric}_{direction}_{signal.get('severity','medium') if isinstance(signal,dict) else getattr(signal,'severity','medium')}",
                    simulation_result=sim_data,
                )
                actions.append(action)

        return actions

    def _compute_action_roi(
        self, category: str, implementation_cost: float, financials: Dict[str, float],
    ) -> Optional[Dict[str, Any]]:
        """
        Compute ROI by actually running the scenario simulation.

        Returns dict with roi_estimate, expected_impact, simulation details,
        or None if simulation fails.
        """
        try:
            from app.services.financial_reasoning import reasoning_engine

            scenario_params = self._CATEGORY_SCENARIO_MAP.get(category, {"ga_pct": -10})

            base = {
                "revenue": financials.get("revenue", 0),
                "cogs": financials.get("cogs", 0),
                "gross_profit": financials.get("gross_profit", 0),
                "ga_expenses": financials.get("ga_expenses", 0),
                "depreciation": financials.get("depreciation", 0),
                "finance_expense": financials.get("finance_expense", 0),
                "tax_rate": financials.get("tax_rate", 0.15),
            }

            result = reasoning_engine.simulate_scenario(
                f"action_roi_{category}", base, scenario_params,
            )

            impact = result.scenario_net_profit - result.base_net_profit
            actual_roi = round(impact / max(implementation_cost, 1), 2)

            return {
                "roi_estimate": actual_roi,
                "expected_impact": round(impact, 0),
                "simulated_net_profit": round(result.scenario_net_profit, 0),
                "base_net_profit": round(result.base_net_profit, 0),
                "simulation_narrative": result.narrative,
                "scenario_params": scenario_params,
                "data_driven": True,
                "computed_from": "financial_reasoning.simulate_scenario",
            }
        except Exception as e:
            logger.debug("Action ROI simulation failed for %s: %s", category, e)
            return None


