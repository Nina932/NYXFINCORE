@dataclass
class SensitivityResult:
    """Monte Carlo sensitivity analysis for a single action."""
    action_id: str
    iterations: int
    mean_roi: float
    median_roi: float
    p10_roi: float          # 10th percentile (downside)
    p90_roi: float          # 90th percentile (upside)
    probability_positive: float  # % of runs with positive outcome
    worst_case_impact: float
    best_case_impact: float
    volatility: float       # std dev of outcomes

    def to_dict(self) -> Dict[str, Any]:
        return {
            "action_id": self.action_id,
            "iterations": self.iterations,
            "mean_roi": round(self.mean_roi, 2),
            "median_roi": round(self.median_roi, 2),
            "p10_roi": round(self.p10_roi, 2),
            "p90_roi": round(self.p90_roi, 2),
            "probability_positive_pct": round(self.probability_positive * 100, 1),
            "worst_case_impact": round(self.worst_case_impact, 2),
            "best_case_impact": round(self.best_case_impact, 2),
            "volatility": round(self.volatility, 4),
        }


