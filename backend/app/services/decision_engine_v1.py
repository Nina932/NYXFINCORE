# ACTION RANKER
# ═══════════════════════════════════════════════════════════════════

class ActionRanker:
    """Ranks actions by deterministic composite score."""

    _URGENCY_MAP = {"critical": 1.0, "high": 0.75, "medium": 0.5, "low": 0.25}
    _FEASIBILITY_MAP = {"immediate": 1.0, "short_term": 0.75, "medium_term": 0.5, "long_term": 0.25}
    _RISK_FACTOR_MAP = {"low": 1.0, "medium": 0.7, "high": 0.4, "critical": 0.2}

    # Weights: ROI matters most, then urgency, feasibility, risk adjustment
    W_ROI = 0.40
    W_URGENCY = 0.25
    W_FEASIBILITY = 0.20
    W_RISK = 0.15

    def rank_actions(self, actions: List[BusinessAction]) -> List[BusinessAction]:
        """
        Compute composite score and sort descending.

        Formula:
            composite = 0.40 * roi_norm + 0.25 * urgency + 0.20 * feasibility + 0.15 * risk_factor
        """
        if not actions:
            return actions

        # Normalize ROI to 0-1 range
        max_roi = max(abs(a.roi_estimate) for a in actions) or 1.0

        for action in actions:
            roi_norm = min(abs(action.roi_estimate) / max_roi, 1.0)

            # Extract severity from source_signal (format: metric_direction_severity)
            parts = action.source_signal.split("_")
            severity = parts[-1] if parts else "medium"
            urgency = self._URGENCY_MAP.get(severity, 0.5)

            feasibility = self._FEASIBILITY_MAP.get(action.time_horizon, 0.5)
            risk_factor = self._RISK_FACTOR_MAP.get(action.risk_level, 0.5)

            action.composite_score = round(
                self.W_ROI * roi_norm
                + self.W_URGENCY * urgency
                + self.W_FEASIBILITY * feasibility
                + self.W_RISK * risk_factor,
                4,
            )

        actions.sort(key=lambda a: a.composite_score, reverse=True)
        return actions


