class DecisionEngine:
    """
    Orchestrates the full decision intelligence pipeline:
        DiagnosticReport -> Actions -> Simulations -> Ranking -> DecisionReport
    """

    def __init__(self):
        self.generator = ActionGenerator()
        self.simulator = ImpactSimulator()
        self.ranker = ActionRanker()
        self.monte_carlo = MonteCarloSimulator()
        self.conviction_scorer = ConvictionScorer()
        self.verdict_builder = VerdictBuilder()
        self._last_report: Optional[DecisionReport] = None
        self._last_verdict: Optional[CFOVerdict] = None

    def generate_decision_report(
        self,
        report,
        financials: Dict[str, float],
        top_n: int = 10,
    ) -> DecisionReport:
        """
        Full pipeline: generate -> simulate -> rank -> report.

        Args:
            report: DiagnosticReport from diagnosis_engine
            financials: Current P&L metrics
            top_n: Max number of top actions to return
        Returns:
            DecisionReport with ranked actions and risk matrix
        """
        try:
            # 1. Generate actions from signals
            actions = self.generator.generate_actions(report, financials)

            # 2. Simulate impact for each action
            for action in actions:
                sim = self.simulator.simulate_action(action, financials)
                action.simulation_result = sim
                # Update ROI from simulation if available
                if sim.get("roi", 0) != 0:
                    action.roi_estimate = sim["roi"]

            # 3. Rank actions
            actions = self.ranker.rank_actions(actions)

            # 4. Build risk matrix
            risk_matrix = self._build_risk_matrix(actions)

            # 5. Compute totals
            total_impact = sum(a.expected_impact for a in actions)
            projected = min(100.0, report.health_score + (total_impact / max(abs(financials.get("revenue", 1)), 1)) * 100)

            # 6. Generate CFO Verdict — the opinionated #1 recommendation
            verdict = self._generate_verdict(actions, report.health_score, financials)
            self._last_verdict = verdict

            result = DecisionReport(
                top_actions=actions[:top_n],
                total_actions_evaluated=len(actions),
                risk_matrix=risk_matrix,
                total_potential_impact=round(total_impact, 2),
                health_score_before=report.health_score,
                projected_health_score=round(projected, 1),
                cfo_verdict=verdict,
                generated_at=datetime.now(timezone.utc).isoformat(),
            )

            self._last_report = result
            logger.info("Decision report generated: %d actions, top composite=%.4f, verdict=%s (conviction=%s)",
                        len(actions), actions[0].composite_score if actions else 0,
                        verdict.conviction_grade, f"{verdict.conviction_score:.0%}")
            return result

        except Exception as e:
            logger.error("Decision report generation failed: %s", e)
            return DecisionReport(
                generated_at=datetime.now(timezone.utc).isoformat(),
            )

    def _generate_verdict(
        self,
        actions: List[BusinessAction],
        health_score: float,
        financials: Dict[str, float],
    ) -> CFOVerdict:
        """Generate the opinionated CFO verdict with Monte Carlo backing."""
        if not actions:
            return CFOVerdict(
                primary_action={},
                conviction_score=0,
                conviction_grade="F",
                verdict_statement="No actionable signals. Monitor and reassess next period.",
                justification=["No significant financial signals detected."],
                risk_acknowledgment="N/A",
                generated_at=datetime.now(timezone.utc).isoformat(),
            )

        top = actions[0]
        runner_up = actions[1] if len(actions) > 1 else None

        # Monte Carlo stress test on #1 pick
        sensitivity = self.monte_carlo.simulate(top, financials)

        # Conviction scoring
        conviction, grade = self.conviction_scorer.score(
            top, runner_up, sensitivity, health_score,
        )

        # Build the final verdict
        return self.verdict_builder.build_verdict(
            actions, sensitivity, conviction, grade, health_score, financials,
        )

    def get_last_report(self) -> Optional[DecisionReport]:
        """Return the most recently generated decision report."""
        return self._last_report

    def get_last_verdict(self) -> Optional[CFOVerdict]:
        """Return the most recently generated CFO verdict."""
        return self._last_verdict

    def _build_risk_matrix(self, actions: List[BusinessAction]) -> RiskMatrix:
        """Group actions by risk level."""
        matrix = RiskMatrix()
        for a in actions:
            summary = {"action_id": a.action_id, "description": a.description,
                        "roi": a.roi_estimate, "score": a.composite_score}
            if a.risk_level == "low":
                matrix.low_risk_actions.append(summary)
            elif a.risk_level == "medium":
                matrix.medium_risk_actions.append(summary)
            elif a.risk_level == "high":
                matrix.high_risk_actions.append(summary)
            else:
                matrix.critical_risk_actions.append(summary)
        return matrix

    def to_kg_entities(self) -> List[Dict[str, Any]]:
        """Generate KG-compatible entities for decision actions."""
        entities = []
        if not self._last_report:
            return entities
        for action in self._last_report.top_actions[:10]:
            entities.append({
                "entity_id": f"decision_{action.action_id}",
                "entity_type": "decision_action",
                "label_en": action.description[:80],
                "label_ka": "",
                "description": (
                    f"Category: {action.category}, ROI: {action.roi_estimate:.1f}x, "
                    f"Risk: {action.risk_level}, Horizon: {action.time_horizon}, "
                    f"Score: {action.composite_score:.4f}"
                ),
                "properties": {
                    "category": action.category,
                    "roi_estimate": action.roi_estimate,
                    "risk_level": action.risk_level,
                    "time_horizon": action.time_horizon,
                    "composite_score": action.composite_score,
                    "expected_impact": action.expected_impact,
                },
            })
        return entities


