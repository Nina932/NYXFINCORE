class ImpactSimulator:
    """Simulates the financial impact of a BusinessAction using the reasoning engine."""

    # Maps action category -> scenario change keys
    _CATEGORY_TO_CHANGES = {
        "cost_reduction": {"cogs_pct": -3.0, "ga_pct": -5.0},
        "revenue_growth": {"revenue_pct": 5.0},
        "risk_mitigation": {"cogs_pct": -1.0},
        "capital_optimization": {"finance_expense_pct": -10.0},
        "operational_efficiency": {"ga_pct": -8.0},
    }

    def simulate_action(self, action: BusinessAction, base_financials: Dict[str, float]) -> Dict[str, Any]:
        """
        Run scenario simulation for a single action.

        Returns:
            Dict with base/scenario comparison and ROI computation.
        """
        try:
            from app.services.financial_reasoning import reasoning_engine

            changes = self._build_changes(action)
            base = self._normalize_base(base_financials)

            result = reasoning_engine.simulate_scenario(
                scenario_name=action.description[:80],
                base=base,
                changes=changes,
            )

            base_np = result.base_net_profit
            scenario_np = result.scenario_net_profit
            delta = round(scenario_np - base_np, 2)
            roi = round(delta / max(action.implementation_cost, 1), 2)

            return {
                "scenario_name": result.scenario_name,
                "base_net_profit": round(base_np, 2),
                "scenario_net_profit": round(scenario_np, 2),
                "net_profit_delta": delta,
                "roi": roi,
                "risk_level": result.risk_level,
                "narrative": result.narrative,
            }
        except Exception as e:
            logger.warning("Impact simulation failed for %s: %s", action.action_id, e)
            return {
                "scenario_name": action.description[:80],
                "base_net_profit": base_financials.get("net_profit", 0),
                "scenario_net_profit": base_financials.get("net_profit", 0),
                "net_profit_delta": 0,
                "roi": 0,
                "risk_level": action.risk_level,
                "narrative": "Simulation unavailable",
            }

    def _build_changes(self, action: BusinessAction) -> Dict[str, float]:
        """Map action category + impact to scenario changes dict."""
        base_changes = self._CATEGORY_TO_CHANGES.get(action.category, {})
        changes = {}
        for key, default_pct in base_changes.items():
            # Scale by ROI — higher ROI actions get more aggressive changes
            scale = min(max(action.roi_estimate / 10.0, 0.5), 2.0)
            changes[key] = round(default_pct * scale, 2)
        return changes

    def _normalize_base(self, financials: Dict[str, float]) -> Dict[str, float]:
        """Ensure base dict has all keys required by simulate_scenario."""
        revenue = financials.get("revenue", 0)
        cogs = financials.get("cogs", 0)
        gp = financials.get("gross_profit", revenue - cogs)
        ga = financials.get("ga_expenses", 0)
        dep = financials.get("depreciation", 0)
        fin = financials.get("finance_expense", 0)
        tax = financials.get("tax_rate", 0.15)
        return {
            "revenue": revenue,
            "cogs": cogs,
            "gross_profit": gp,
            "ga_expenses": ga,
            "depreciation": dep,
            "finance_expense": fin,
            "tax_rate": tax,
        }


